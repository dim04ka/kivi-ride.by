    <p><i>Скорость и качество</i></p>
    <p>Kivi ride предоставляет возможность получить желаемое в короткий срок. Немедленно нужно передать посылку или оказаться в другом конце города? Мы доставим в лучшем виде!</p>
    <p>Новая система автоматически подыщет ближайшее такси или курьера, и доставка к пункту назначения товара или пассажира будет максимально оперативной.</p>
    <p><i>Обратная связь</i></p>
    <p>Мы стараемся учесть все пожелания и готовы к изменениям, так как гибкость – одно из наших свойств. Необходимо доставить подарок и передать устное поздравление? Добавьте эту ремарку в качестве примечания к заказу, и все будет исполнено в соответствии с Вашими пожеланиями.</p>
    <p>Kivi ride стремится слышать и надеется быть услышанным. Если у Вас есть идеи по улучшению нашего сервиса или замечания – мы внимательно их изучим и будем совершенствоваться вместе с Вами!</p>
