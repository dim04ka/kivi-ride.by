    <p>Мы решили улучшить ситуацию и запустить комплексный сервис для заказа такси и доставки товаров - Kivi ride! Теперь у гостей и жителей Минска есть возможность вызвать такси удобным способом, а также получить доставку на дом или в офис.</p>
    <p>Наши курьеры привезут все: от документов до цветов или продуктов. При этом степень социальных контактов сведена к минимуму, а качество и безопасность работы гарантируется.</p>
    <p>Еще одно новшество: доставка груза с Kivi ride может осуществляться через нашего курьера как из определенной точки, так и из нескольких мест сразу. То есть Вы можете что-то заказать не только в магазине или на почте. При этом стоимость услуги рассчитывается предварительно и не меняется по окончанию работы.</p>
    <p>Также мы работаем с корпоративными клиентами для доставки товара клиентам или сотрудников в офис и домой.</p>
