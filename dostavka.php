                  <li>- оставьте комментарий для уточнений Вашего заказа, если нужно.</li>
                </ul>
                <p>2. Вам нужно что-то привезти.</p>
                <ul>
                  <li>- в форме заказа в графе «Откуда везем» введите адрес желаемого магазина, или, если не важно из какого магазина, вызывайте просто на свой адрес курьера с комментарием – «продукты из магазина»;</li>
